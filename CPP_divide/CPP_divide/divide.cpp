#include<iostream>
int main() {
	using namespace std;
	cout.setf(ios_base::fixed, ios_base::floatfield);
	cout << "정수 나눗셈: 9/5 = " << 9 / 5 << endl;//소수부는 버려짐
	cout << "부동 소수점수 나눗셈: 9.0/5.0 = ";
	cout << 9.0 / 5.0  << endl;//기본적으론 double형
	cout << "혼합 나누셈: 9.0/5 = " << 9.0 / 5 << endl;
	cout << "double형 상수: 1e7/9.0 = ";
	cout << 1.e7 / 9.0 << endl;//f표시가없으면 double형으로 인식함.
	cout << "float형 상수: 1e7f/9.0f = ";
	cout << 1.e7f / 9.0f << endl;//뒤에 f를붙여 float임을 표기하면 float형으로 표기
	return 0;
}